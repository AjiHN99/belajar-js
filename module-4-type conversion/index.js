// Type Conversion
// implicit conversion

let  result = "10" + 10; // akan di konversi ke string
console.log(result, typeof result); // 1010 string

// falsy value
let nilai = !0;

console.logo(nilai, typeof nilai); // true boolean

// explicit conversion
let nilaiString = String(123);
console.log(nilaiString, typeof nilaiString); // "123" string


